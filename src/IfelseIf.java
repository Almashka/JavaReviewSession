public class IfelseIf {
    public static void main(String[] args) {
        int i = 10;
        if(i == 10){
            int b =12;
            System.out.println(i + " is 10");
            System.out.println(b + " is 12");
        }
        else if(i == 15){
            System.out.println(i + " is 15");
        }
        else if(i == 20){
            System.out.println(i + " is 20");
        }
        else{
            System.out.println(i + " is not present");
        }
    }
}
